my_list = [1,2,3,4,5,6]

#append(): Adds an element to the end of the list
my_list.append(7)
print(my_list)

#extend(): Extend the element of an iterable to the end

my_list.extend([8,9])
print(my_list)

#insert(index, object): insert element at position
my_list.insert(1, 0)
print(my_list)

#remove(): remove the first occurrence of element
number_list: [1,2,3,4,5,4,2,1]
number_list.remove(1)
print(number_list)